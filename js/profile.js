function loadDistrictOptions(state) {
    const districtSelect = document.getElementById('district');
    districtSelect.innerHTML = '<option value="">Select District</option>';
    if (state && districtMap[state]) {
        districtMap[state].forEach(district => {
            const option = document.createElement('option');
            option.value = district;
            option.text = district;
            districtSelect.appendChild(option);
        });
        districtSelect.disabled = false;
    } else {
        districtSelect.disabled = true;
    }
}

function loadStateOptions() {
    const stateSelect = document.getElementById('state');
    stateSelect.innerHTML = '<option value="">Select State</option>';
    Object.keys(districtMap).forEach(state => {
        const option = document.createElement('option');
        option.value = state;
        option.text = state;
        stateSelect.appendChild(option);
    });
}

function loadUserData() {
    console.log('Loading user data');
    // Get user data from both possible sources
    const userData = JSON.parse(localStorage.getItem('currentUser')) || 
                    JSON.parse(localStorage.getItem('userData')) || {};

    console.log('User data:', userData);

    // Fill form fields with user data
    const fields = [
        'firstName', 'lastName', 'email', 'phone',
        'farmName', 'farmSize', 'state', 'district',
        'farmType', 'mainCrop'
    ];

    fields.forEach(field => {
        const element = document.getElementById(field);
        if (element && userData[field]) {
            element.value = userData[field];
            console.log(`Setting ${field} to ${userData[field]}`);
        }
    });

    // If state is selected, load its districts
    if (userData.state) {
        loadDistrictOptions(userData.state);
        // Set district if it exists in user data
        if (userData.district) {
            const districtSelect = document.getElementById('district');
            if (districtSelect) {
                districtSelect.value = userData.district;
            }
        }
    }
}

function handleProfileSubmit() {
    console.log('Handling profile submit');
    
    // Add loading state
    const submitButton = document.querySelector('button[type="submit"]');
    const originalText = submitButton.textContent;
    submitButton.textContent = 'Saving...';
    submitButton.disabled = true;
    
    try {
        // Get all form data
        const formData = {
            firstName: document.getElementById('firstName').value,
            lastName: document.getElementById('lastName').value,
            email: document.getElementById('email').value,
            phone: document.getElementById('phone').value,
            farmName: document.getElementById('farmName').value,
            farmSize: document.getElementById('farmSize').value,
            state: document.getElementById('state').value,
            district: document.getElementById('district').value,
            farmType: document.getElementById('farmType').value,
            mainCrop: document.getElementById('mainCrop').value,
            lastUpdated: new Date().toISOString()
        };

        console.log('Form data to save:', formData);

        // Validate required fields
        const requiredFields = ['firstName', 'lastName', 'email', 'state', 'district'];
        const missingFields = requiredFields.filter(field => !formData[field]);
        
        if (missingFields.length > 0) {
            throw new Error(`Please fill in all required fields: ${missingFields.join(', ')}`);
        }

        // Validate email format
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailRegex.test(formData.email)) {
            throw new Error('Please enter a valid email address');
        }

        // Save to both storage keys to maintain compatibility
        localStorage.setItem('currentUser', JSON.stringify(formData));
        localStorage.setItem('userData', JSON.stringify(formData));

        // Show success message
        showSuccessMessage('Profile updated successfully!');

        // Update welcome message
        updateWelcomeMessage(formData.firstName);

        // Reset loading state
        submitButton.textContent = originalText;
        submitButton.disabled = false;

        return false;
    } catch (error) {
        console.error('Error saving profile:', error);
        showErrorMessage(error.message || 'Failed to save profile. Please try again.');
        
        // Reset loading state
        submitButton.textContent = originalText;
        submitButton.disabled = false;
    }
}

function resetForm() {
    if (confirm('Are you sure you want to reset the form? All unsaved changes will be lost.')) {
        loadUserData(); // Reload the original data
    }
}

function showSuccessMessage(message) {
    const successDiv = document.createElement('div');
    successDiv.className = 'success-message';
    successDiv.textContent = message;
    successDiv.style.cssText = `
        background: #d4edda;
        color: #155724;
        padding: 15px;
        border-radius: 5px;
        margin-bottom: 20px;
        display: block;
        animation: fadeIn 0.3s ease-in;
    `;

    // Insert at the top of the form
    const form = document.getElementById('profileForm');
    if (form) {
        // Remove any existing messages
        const existingMessage = form.querySelector('.success-message');
        if (existingMessage) {
            existingMessage.remove();
        }
        
        form.insertBefore(successDiv, form.firstChild);

        // Remove after 3 seconds
        setTimeout(() => {
            successDiv.style.opacity = '0';
            setTimeout(() => successDiv.remove(), 300);
        }, 3000);
    }
}

